      <div class="tab-pane fade" id="list-messages" role="tabpanel" aria-labelledby="list-messages-list">De plus en plus des applications informatiques sont conçues pour fonctionner sur des réseaux d’ordinateurs coopérants. La consolidation de l’Internet mondial, l’émergence du Cloud Computing, les nouvelles technologies des réseaux de communications sans fils, et la disponibilité d’ordinateurs toujours moins chers et plus puissants permettent la construction d’une nouvelle génération d’applications distribuées. Ces applications envahissent l’industrie, le commerce, la santé, l’éducation et, en effet, la vie personnelle comme l’intégration de la technologie informatique et des communications numériques. Il y a quelques aspects de la vie qui ne sont pas améliorés par les réseaux et systèmes di stribués, mais qui doivent aussi être tempérés par les préoccupations de la sécurité et la durabilité de l’énergie. Ce Master « Réseaux et Systèmes distribués » offre une formation aussi bien théorique que pratique dans le domaine des réseaux et des systèmes distribués. De ce fait, les étudiants ayant validé la première année dans l’une des formations RSD, GL et STIC peuvent passer d’une spécialité à une autre. Ce passage nécessite l’acquisition de modules spécifiques à la formation choisie.</div>
